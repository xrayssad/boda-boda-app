// Fast search function
export const searchLocations = (query) => {
  if (!query || query.length < 2) return [];
  
  const searchTerm = query.toLowerCase();
  const results = tanzaniaLocations.filter(location => 
    location.name.toLowerCase().includes(searchTerm) ||
    location.address.toLowerCase().includes(searchTerm) ||
    location.type.toLowerCase().includes(searchTerm)
  );
  
  // Return max 15 results, sorted by relevance
  return results.sort((a, b) => {
    const aNameMatch = a.name.toLowerCase().startsWith(searchTerm);
    const bNameMatch = b.name.toLowerCase().startsWith(searchTerm);
    if (aNameMatch && !bNameMatch) return -1;
    if (!aNameMatch && bNameMatch) return 1;
    return 0;
  }).slice(0, 15);
};
